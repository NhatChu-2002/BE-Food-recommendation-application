package com.example.BEFoodrecommendationapplication.repository;

import com.example.BEFoodrecommendationapplication.entity.FoodRecipe;
import com.example.BEFoodrecommendationapplication.entity.RecentSearch;
import com.example.BEFoodrecommendationapplication.entity.User;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.JpaSpecificationExecutor;
import org.springframework.data.rest.core.annotation.RepositoryRestResource;

import java.util.List;
import java.util.Optional;

@RepositoryRestResource(exported = false)
public interface RecentSearchRepository extends JpaRepository<RecentSearch, Integer> {
    List<RecentSearch> findTop10ByUser_IdOrderByTimestampDesc(Integer userId);

    Optional<RecentSearch> findByUserAndRecipe(User user, FoodRecipe recipe);
}
